import sharp from 'sharp';
import { mkdirSync } from 'fs';
import { join } from 'path';

const SIZE = 48;
const OUT = join(import.meta.dirname, '..', 'sprites');
mkdirSync(OUT, { recursive: true });

const C = {
  _: [0, 0, 0, 0],
  O: [90, 65, 45, 255],
  B: [160, 120, 80, 255],
  L: [185, 145, 100, 255],
  D: [120, 85, 55, 255],
  N: [50, 35, 25, 255],
  E: [25, 20, 15, 255],
  W: [240, 235, 225, 255],
  P: [195, 155, 130, 255],
  H: [255, 180, 180, 255],
  Z: [200, 200, 220, 255],
  Y: [255, 220, 100, 255],
  R: [220, 80, 80, 255],
  X: [180, 50, 50, 255],      // X eyes (dead)
  F: [130, 140, 130, 255],    // fly color
  U: [100, 180, 220, 255],    // tear
  K: [140, 140, 140, 255],    // gray (dead body)
  M: [110, 110, 110, 255],    // gray dark (dead outline)
  V: [160, 160, 160, 255],    // gray light (dead belly)
};

function parseFrame(template) {
  const lines = template.split('\n').filter(l => l.includes('|'));
  return lines.map(line => {
    const inner = line.split('|')[1];
    const pixels = [];
    for (let i = 0; i < inner.length; i += 1) pixels.push(inner[i]);
    return pixels;
  });
}

function renderFrame(parsed, size) {
  const buf = Buffer.alloc(size * size * 4, 0);
  for (let y = 0; y < parsed.length && y < size; y++) {
    for (let x = 0; x < parsed[y].length && x < size; x++) {
      const key = parsed[y][x];
      const color = C[key] || C._;
      const idx = (y * size + x) * 4;
      buf[idx] = color[0]; buf[idx+1] = color[1]; buf[idx+2] = color[2]; buf[idx+3] = color[3];
    }
  }
  return buf;
}

async function createSheet(name, frames) {
  const frameCount = frames.length;
  const parsed = frames.map(f => parseFrame(f));
  const totalWidth = SIZE * frameCount;
  const strip = Buffer.alloc(totalWidth * SIZE * 4, 0);
  for (let f = 0; f < frameCount; f++) {
    const frameBuf = renderFrame(parsed[f], SIZE);
    for (let y = 0; y < SIZE; y++) {
      const srcOffset = y * SIZE * 4;
      const dstOffset = (y * totalWidth + f * SIZE) * 4;
      frameBuf.copy(strip, dstOffset, srcOffset, srcOffset + SIZE * 4);
    }
  }
  const outPath = join(OUT, `clamber-${name}.png`);
  await sharp(strip, { raw: { width: totalWidth, height: SIZE, channels: 4 } })
    .png().toFile(outPath);
  console.log(`  ✓ ${name}: ${frameCount} frames`);
}

