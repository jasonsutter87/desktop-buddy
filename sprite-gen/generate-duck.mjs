import sharp from 'sharp';
import { mkdirSync } from 'fs';
import { join } from 'path';

const SIZE = 48;
const OUT = join(import.meta.dirname, '..', 'sprites');
mkdirSync(OUT, { recursive: true });

const C = {
  _: [0, 0, 0, 0],
  D: [220, 185, 50, 255],    // duck outline/darker yellow
  d: [255, 220, 80, 255],    // duck body yellow
  l: [255, 235, 140, 255],   // duck light/belly
  o: [255, 140, 50, 255],    // beak orange
  O: [220, 110, 30, 255],    // beak dark
  E: [25, 20, 15, 255],      // eye
  W: [240, 235, 225, 255],   // eye highlight
  H: [255, 180, 180, 255],   // heart/blush
  Y: [255, 220, 100, 255],   // sparkle
  R: [220, 80, 80, 255],     // surprise
  Z: [200, 200, 220, 255],   // zzz
  U: [100, 180, 220, 255],   // tear
  G: [120, 180, 80, 255],    // food green
  S: [100, 75, 50, 255],     // poop
  T: [80, 60, 40, 255],      // poop dark
  F: [130, 140, 130, 255],   // fly
  K: [180, 180, 130, 255],   // dead body
  M: [150, 150, 100, 255],   // dead outline
  V: [200, 200, 160, 255],   // dead light
  X: [180, 50, 50, 255],     // X eyes
};

function parseFrame(t) {
  return t.split('\n').filter(l => l.includes('|')).map(l => {
    const inner = l.split('|')[1];
    const px = []; for (let i = 0; i < inner.length; i++) px.push(inner[i]); return px;
  });
}
function renderFrame(parsed, size) {
  const buf = Buffer.alloc(size * size * 4, 0);
  for (let y = 0; y < parsed.length && y < size; y++)
    for (let x = 0; x < parsed[y].length && x < size; x++) {
      const c = C[parsed[y][x]] || C._; const i = (y * size + x) * 4;
      buf[i] = c[0]; buf[i+1] = c[1]; buf[i+2] = c[2]; buf[i+3] = c[3];
    }
  return buf;
}
async function sheet(name, frames) {
  const fc = frames.length, p = frames.map(f => parseFrame(f)), tw = SIZE * fc;
  const s = Buffer.alloc(tw * SIZE * 4, 0);
  for (let f = 0; f < fc; f++) { const fb = renderFrame(p[f], SIZE);
    for (let y = 0; y < SIZE; y++) { const so = y*SIZE*4, d = (y*tw+f*SIZE)*4; fb.copy(s,d,so,so+SIZE*4); }}
  await sharp(s, {raw:{width:tw,height:SIZE,channels:4}}).png().toFile(join(OUT,`duck-${name}.png`));
  console.log(`  ✓ duck-${name}: ${fc} frames`);
}

